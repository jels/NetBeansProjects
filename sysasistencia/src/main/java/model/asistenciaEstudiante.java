    public void setFechaIngresoEs(String fechaIngresoEs) {
        this.fechaIngresoEs = fechaIngresoEs;
    }

    public String getHoraIngresoEs() {
        return horaIngresoEs;
    }

    public void setHoraIngresoEs(String horaIngresoEs) {
        this.horaIngresoEs = horaIngresoEs;
    }

    public String getHoraSalidaEs() {
        return horaSalidaEs;
    }

    public void setHoraSalidaEs(String horaSalidaEs) {
        this.horaSalidaEs = horaSalidaEs;
    }

