    private String codigoProf;
    private String fechaIngresoProf;
    private String horaIngresoProf;
    private String horaSalidaProf;
    private String estadoProfAs;
    private String turnoProf;
    private String marcaProf;

    public int getIdAsistenciaProf() {
        return idAsistenciaProf;
    }

